    {
      Error_Handler();
    }

    printf("Wake-up \r\n");
    memset(adc_data, 0x00, sizeof(adc_data));
    HAL_Delay(100);
    printf("Going to sleep : %d, %d\r\n", (int)adc_data[0], (int)HAL_LPTIM_ReadCounter(&hlptim1));
    HAL_Delay(100);
    printf("Going to sleep : %d, %d\r\n", (int)adc_data[0], (int)HAL_LPTIM_ReadCounter(&hlptim1));
